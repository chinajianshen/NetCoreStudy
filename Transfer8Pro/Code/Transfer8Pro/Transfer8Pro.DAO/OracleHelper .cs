        static Regex R_Cols = new Regex(@"insert\s+into\s+(.*?)\((.*?)\)\s+values\s*\((?<T>.*?)\)", RegexOptions.Compiled | RegexOptions.Singleline);
        static Regex R_Cols_Update = new Regex(@":(.*?)[\s,]+", RegexOptions.Compiled | RegexOptions.Singleline);

        private static List<string> ParseFileds(string sql)
        {
            List<string> rt = new List<string>();
            if (sql.StartsWith("insert"))
            {
                Match mc = R_Cols.Match(sql);
                if (mc.Success)
                {
                    string[] parts = mc.Groups["T"].Value.Split(new char[] { ',' }, StringSplitOptions.RemoveEmptyEntries);

                    foreach (string p in parts)
                    {
                        rt.Add(p.Trim());
                    }
                    return rt;
                }
                else
                    return new List<string>();
            }
            else if (sql.StartsWith("update") || sql.StartsWith("delete"))
            {
                Match mc = R_Cols_Update.Match(sql);
                while (mc.Success)
                {
                    rt.Add(string.Format(":{0}", mc.Groups[1].Value.Trim()));
                    mc = mc.NextMatch();
                }
                return rt;
            }
            else
                return new List<string>();

        }
        private static dynamic AutoCreatePrms(string filed, DataTable dtsource)
        {
            Type t = dtsource.Columns[filed].DataType;
            if (t == typeof(int))
            {
                List<int> lists = new List<int>();
                foreach (DataRow dr in dtsource.Rows)
                {
                    lists.Add(int.Parse(dr[filed].ToString()));
                }
                return lists;
            }
            else if (t == typeof(float))
            {
                List<float> lists = new List<float>();
                foreach (DataRow dr in dtsource.Rows)
                {
                    lists.Add(float.Parse(dr[filed].ToString()));
                }
                return lists;
            }
            else if (t == typeof(double))
            {
                List<double> lists = new List<double>();
                foreach (DataRow dr in dtsource.Rows)
                {
                    lists.Add(double.Parse(dr[filed].ToString()));
                }
                return lists;
            }
            else if (t == typeof(DateTime))
            {
                List<DateTime> lists = new List<DateTime>();
                foreach (DataRow dr in dtsource.Rows)
                {
                    lists.Add(DateTime.Parse(dr[filed].ToString()));
                }
                return lists;
            }
            else if (t == typeof(Guid))
            {
                List<Guid> lists = new List<Guid>();
                foreach (DataRow dr in dtsource.Rows)
                {
                    lists.Add(Guid.Parse(dr[filed].ToString()));
                }
                return lists;
            }
            else
            {
                List<string> lists = new List<string>();
                foreach (DataRow dr in dtsource.Rows)
                {
                    lists.Add(dr[filed].ToString());
                }
                return lists;
            }
        }
        private static List<OracleParameter> CreateBatchParamters(string sql, DataTable dtsource)
        {
            List<string> rt = ParseFileds(string.Format("{0} ", sql.ToLower().Trim()));
            List<OracleParameter> ps = new List<OracleParameter>();

            foreach (var filed in rt)
            {
                string _filed = filed.Replace(":", "");
                dynamic values = AutoCreatePrms(_filed, dtsource);
                OracleParameter o = new OracleParameter(filed, values.ToArray());
                ps.Add(o);

            }
            return ps;
        }

