        public static int ExecuteNonQueryBatch(OracleTransaction transaction, string commandText, DataTable datasource)
        {
            try
            {
                var commandParameters = CreateBatchParamters(commandText, datasource);
                if (transaction == null) throw new ArgumentNullException("transaction");
                if (transaction != null && transaction.Connection == null) throw new ArgumentException("The transaction was rollbacked or commited, please provide an open transaction.", "transaction");

                // Create a command and prepare it for execution
                OracleCommand cmd = new OracleCommand();
                bool mustCloseConnection = false;
                PrepareCommand(cmd, transaction.Connection, transaction, CommandType.Text, commandText, commandParameters.ToArray(), out mustCloseConnection);
                cmd.ArrayBindCount = datasource.Rows.Count;
                // Finally, execute the command
                int retval = cmd.ExecuteNonQuery();

                // Detach the SqlParameters from the command object, so they can be used again
                cmd.Parameters.Clear();
                return retval;
            }
            catch (Exception ex)
            {
                LogUtil.WriteLog(ex);
                return -1;
            }
        }


