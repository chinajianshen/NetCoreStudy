        public static int ExecuteNonQueryBatch(string connectstring, string sql, DataTable datasource)
        {
            OracleTransaction trans = null;
            using (OracleConnection con = new OracleConnection(connectstring))
            {
                try
                {
                    con.Open();
                    trans = con.BeginTransaction();
                    int retv = ExecuteNonQueryBatch(trans, sql, datasource);
                    if (retv >= 0)
                    {
                        trans.Commit();
                        return retv;
                    }
                    else
                    {
                        trans.Rollback();
                        return -1;
                    }
                }
                catch
                {
                    if (trans != null)
                    {
                        trans.Rollback();
                    }
                    return -1;
                }
            }
